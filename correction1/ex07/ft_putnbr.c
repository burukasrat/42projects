#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	c;

	if (nb == 0)
		return ;
	c = nb % 10;
	nb = nb / 10;
	ft_putnbr(nb);
	c += 48;
	write(1, &c, 1);
}
