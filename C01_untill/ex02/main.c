#include <unistd.h>

void	ft_swap(int *a, int *b);

int	main(void)
{
	int	a;
	int	b;

	a = 4;
	b = 9;
	ft_swap(&a, &b);
	a += 48;
	b += 48;
	write(1, &a, 1);
	write(1, &b, 1);
	return (0);
}
